package com.basics;

public class BreakEx {
	

	    public static void main(String[] args) {

	        // Loop from 1 to 10
	        for(int i = 1; i <= 10; i++){

	            // If i becomes 5
	            if(i == 5){
	                break;   // stop the loop immediately
	            }

	            // Print the value of i
	            System.out.println(i);

	        }

	    }

	}

